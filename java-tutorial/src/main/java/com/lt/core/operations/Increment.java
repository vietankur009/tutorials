package com.lt.core;

public class Increment {
    public static void main(String... arg){

        int i=18;
        System.out.println(i+1);
        System.out.println(--i);
        System.out.println(i-1);
        System.out.println(++i);
        int c= 15;
        System.out.println(++c);
        System.out.println(--c);
        System.out.println(c+1);
        System.out.println(c-1);

    }
}
