package main.java.com.lt.core.ankit.pojo;

public class TrainDetails {
    private String trainName;
    private String roat;
    private int trainNo;
    private String firstStation;
    private String firstCochName;
    private int passengerDetails;
    private int firstCochPassengerDetails;
    private int AllCochQuainty;

    public String getTrainName() {
        return trainName;
    }

    public void setTrainName(String trainName) {
        this.trainName = trainName;
    }

    public String getRoat() {
        return roat;
    }

    public void setRoat(String roat) {
        this.roat = roat;
    }

    public int getTrainNo() {
        return trainNo;
    }

    public void setTrainNo(int trainNo) {
        this.trainNo = trainNo;
    }

    public String getFirstStation() {
        return firstStation;
    }

    public void setFirstStation(String firstStation) {
        this.firstStation = firstStation;
    }

    public String getFirstCochName() {
        return firstCochName;
    }

    public void setFirstCochName(String firstCochName) {
        this.firstCochName = firstCochName;
    }

    public int getPassengerDetails() {
        return passengerDetails;
    }

    public void setPassengerDetails(int passengerDetails) {
        this.passengerDetails = passengerDetails;
    }

    public int getFirstCochPassengerDetails() {
        return firstCochPassengerDetails;
    }

    public void setFirstCochPassengerDetails(int firstCochPassengerDetails) {
        this.firstCochPassengerDetails = firstCochPassengerDetails;
    }

    public int getAllCochQuainty() {
        return AllCochQuainty;
    }

    public void setAllCochQuainty(int allCochQuainty) {
        AllCochQuainty = allCochQuainty;
    }

}
