package main.java.com.lt.core.ankit.pojo;


public class FlightDetails {
    private String flightName;
    private int holidaysPlane;
    private double checkinTime;
    private String departureCity;
    private String arrivalCity;
    private int departDate;
    private int flightNo;

    public String getFlightName() {
        return flightName;
    }

    public void setFlightName(String flightName) {
        this.flightName = flightName;
    }

    public int getHolidaysPlane() {
        return holidaysPlane;
    }

    public void setHolidaysPlane(int holidaysPlane) {
        this.holidaysPlane = holidaysPlane;
    }

    public double getCheckinTime() {
        return checkinTime;
    }

    public void setCheckinTime(double checkinTime) {
        this.checkinTime = checkinTime;
    }

    public String getDepartureCity() {
        return departureCity;
    }

    public void setDepartureCity(String departureCity) {
        this.departureCity = departureCity;
    }

    public String getArrivalCity() {
        return arrivalCity;
    }

    public void setArrivalCity(String arrivalCity) {
        this.arrivalCity = arrivalCity;
    }

    public int getDepartDate() {
        return departDate;
    }

    public void setDepartDate(int departDate) {
        this.departDate = departDate;
    }

    public int getFlightNo() {
        return flightNo;
    }

    public void setFlightNo(int flightNo) {
        this.flightNo = flightNo;
    }
}
