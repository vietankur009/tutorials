public class Universitys {

    private String name;
    private String universityName;
    private String universityAdderess;
    private String universityCorrecess;
    private int bscStudent;
    private int mathesStudent;
    private int physicsStudent;
    private String principalName;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUniversityName() {
        return universityName;
    }

    public void setUniversityName(String universityName) {
        this.universityName = universityName;
    }

    public String getUniversityAdderess() {
        return universityAdderess;
    }

    public void setUniversityAdderess(String universityAdderess) {
        this.universityAdderess = universityAdderess;
    }

    public String getUniversityCorrecess() {
        return universityCorrecess;
    }

    public void setUniversityCorrecess(String universityCorrecess) {
        this.universityCorrecess = universityCorrecess;
    }

    public int getBscStudent() {
        return bscStudent;
    }

    public void setBscStudent(int bscStudent) {
        this.bscStudent = bscStudent;
    }

    public int getMathesStudent() {
        return mathesStudent;
    }

    public void setMathesStudent(int mathesStudent) {
        this.mathesStudent = mathesStudent;
    }

    public int getPhysicsStudent() {
        return physicsStudent;
    }

    public void setPhysicsStudent(int physicsStudent) {
        this.physicsStudent = physicsStudent;
    }

    public String getPrincipalName() {
        return principalName;
    }

    public void setPrincipalName(String principalName) {
        this.principalName = principalName;
    }
}
